	; Loop through all 
	; ^LAHM(62.48,DA(1),"SCT","AD",CODE,VARPTR,DA)
	; #62.48 only supports SCT code sets
	; (private method for LA7SRPT2)
	N NODE,NODE1,POS,R6248,R624802,STATUS,D61,D62,X,DATA,REC,FILE
	S CODE=$G(CODE)
	S SYS=$G(SYS)
